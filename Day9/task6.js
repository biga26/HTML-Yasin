const calculator = {
    addition: function(num1, num2) {
        return num1 + num2
    },
    substraction: function(num1, num2){
        return num1 - num2;
    },

    multiplication: function(num1, num2){
        return num1 * num2;
    },

    division: function(num1, num2){
        return num1 / num2;
    },


}

